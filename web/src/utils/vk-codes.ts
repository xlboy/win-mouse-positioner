const webKCodeMap = invert(vkCodeMap);

/**
 * Get WebDefaultCodes by  VKCode
 */
export function getWebKCodeByVKCode<T = DefaultCodes>(vkCode: VKCode) {
  return webKCodeMap[vkCode] as T | undefined;
}

export function getVKCodeByWebKCode(code: DefaultCodes) {
  return vkCodeMap[code];
}
